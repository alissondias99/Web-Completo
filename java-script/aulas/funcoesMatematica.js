var x = 10.380;
console.log(x);
console.log(Math.ceil(x));//Math é um objeto nativo do JavaScript que contém várias propriedades e métodos relacionados a tarefas matemáticas. O método ceil() é útil para arredondar um número para cima.
console.log(Math.floor(x));//Já o `Math.floor()` pega a parte inteira de um número, arredondando-o para baixo.
console.log(Math.random());//O Math.random() naturalmente exibe um valor aleatório entre 0 e 1. No entanto, ele pode ser utilizado para criar um randomizador de números ao ser combinado com outras funções matemáticas.
